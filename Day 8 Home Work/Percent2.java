class Percent2
{
	public static void main(String[] args) 
	{
        int num = 1500;
        int percent = 65;
        int increase = num * percent / 100;
        int result = num - increase;
        System.out.println("Original value: " + num);
        System.out.println("After 30% Decrease: " + result);
    }
}
