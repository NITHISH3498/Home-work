class Unary
{
	public static void main(String[] args)
	{
		int num = 5;
		System.out.println("Initial value :"+num);
		System.out.println("After num++ :"+ num++);
		System.out.println("After ++num :"+ ++num);
		System.out.println("After num-- :"+ num--);
		System.out.println("After --num :"+ --num);
	}
}